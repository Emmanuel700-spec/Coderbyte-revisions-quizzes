def sort_array(arr):
    """
    Sort the array in ascending order.

    Parameters:
    - arr: List of integers.

    Returns:
    - A sorted list in ascending order.
    """
    return sorted(arr)

# Example usage
input_array = [5, 1, 3, 4, 2, 8, 7, 6, 10]
print(f"Sorted array: {sort_array(input_array)}")
